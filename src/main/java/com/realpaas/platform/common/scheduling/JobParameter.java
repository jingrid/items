public class JobParameter implements Serializable{
    
    private static final long serialVersionUID = 5096811018696644776L;

    /*
     * Job parameter's value which will be stored to database
     */
    private String value;
    
    /*
     * Job parameter's name which describes what the parameter means, and 
     * tells what the parameterized job really does.
     */
    private String name;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
