public class UserInformation implements Serializable {

    private static final long serialVersionUID = 2903689543666529122L;

    private String id;
    
    private String displayName;
    
    private String email;
    
    private UserAuthenticationInformation currentAuthenticationInformation;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public UserAuthenticationInformation getCurrentAuthenticationInformation() {
        return currentAuthenticationInformation;
    }

    public void setCurrentAuthenticationInformation(UserAuthenticationInformation currentUserAccess) {
        this.currentAuthenticationInformation = currentUserAccess;
    }
    
}
